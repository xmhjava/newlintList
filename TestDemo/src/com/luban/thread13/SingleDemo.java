//懒汉式

//加入同步为了解决多线程安全问题。
//加入双重判断是为了解决效率问题。

class Singletest
{
	private static Singletest s = null;

	private Singletest(){}

	public static Singletest getInstance()
	{
		if(s==null)
		{
			synchronized(Singletest.class)		
			{
				if(s==null)
		//				-->0 -->1
					s = new Singletest();
			}
		}
		return s;
	}
}